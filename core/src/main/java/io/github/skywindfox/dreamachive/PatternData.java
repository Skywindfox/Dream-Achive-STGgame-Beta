package io.github.skywindfox.dreamachive;

public class PatternData {
    private String type;
    private Float interval;
    private Float baseInterval;
    private Float amplitude;
    private Float frequency;

    // Getters and setters
    public String getType() { return type; }
    public void setType(String type) { this.type = type; }
    public Float getInterval() { return interval; }
    public void setInterval(Float interval) { this.interval = interval; }
    public Float getBaseInterval() { return baseInterval; }
    public void setBaseInterval(Float baseInterval) { this.baseInterval = baseInterval; }
    public Float getAmplitude() { return amplitude; }
    public void setAmplitude(Float amplitude) { this.amplitude = amplitude; }
    public Float getFrequency() { return frequency; }
    public void setFrequency(Float frequency) { this.frequency = frequency; }
}